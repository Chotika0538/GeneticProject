    private void setBackbtn(JButton b,JLabel l1,JLabel l2,JLabel l3,JLabel l4){
        if(b!=null){
            b.setVisible(true);
            b.setEnabled(true);
        }
        try{
            l1.setVisible(false);
            l1.setEnabled(false);
        }catch(Exception e ){System.out.println(e);}
        try{
            l2.setVisible(false);
            l2.setEnabled(false);
        }catch(Exception e){System.out.println(e);}
        try{
            l3.setVisible(false);
            l3.setEnabled(false);
        }catch(Exception e){System.out.println(e);}
        try{
            l4.setVisible(false);
            l4.setEnabled(false);
        }catch(Exception e){System.out.println(e);}
        
    }
